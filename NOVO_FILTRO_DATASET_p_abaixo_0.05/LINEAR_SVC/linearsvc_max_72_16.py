import pandas as pd
dados = pd.read_csv("Testes_ML_classi_filtro2.csv")
dados.head()

x = dados[["Sexo","trabalha","pos_estudos","saude","engravidou" ,"Solitude","Fumou","Pais_Fumante","amigod_drogados","idade"]]
y = dados[["Ja_bebeu"]]

import numpy as np
from sklearn.model_selection import train_test_split
from sklearn.svm import LinearSVC
from sklearn.metrics import accuracy_score

i = 0.10 # Tamanho mínimo para teste 

vetor_test =[] # Vai guardar os testes em %
acu =[]   # acuracias/ acertos 


while i<1: 

  SEED = 5
  np.random.seed(SEED)
  treino_x, teste_x, treino_y, teste_y = train_test_split(x, y, test_size = i , stratify = y)
  modelo = LinearSVC()
  modelo.fit(treino_x, treino_y)
  previsoes = modelo.predict(teste_x)
  acuracia = accuracy_score(teste_y, previsoes) * 100

  acu.append(acuracia)
  vetor_test.append(i)
  i = i + 0.01
  

acu

n_max = (max(acu)) # Melhor valor para acuracia entre um intervalo de test_size de 0.10 até 0.99
n_pos = acu.index(n_max) # Posição do melhor valor


print(max(acu)) # valor da maior acuracia, 72,16%
print(n_pos) # Posição da maior acuracia, 0 
print(vetor_test[0]) # tamanho do treino, 10%