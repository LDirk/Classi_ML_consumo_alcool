import pandas as pd
dados = pd.read_csv("Testes_ML_classi_filtro2.csv")

del dados["engravidou"]
# tirando a coluna ja engravidou para esse modelo, pois possui valores negativos
dados.head()

x = dados[["Sexo","trabalha","pos_estudos","saude","Solitude","Fumou","Pais_Fumante","amigod_drogados","idade"]]
y = dados[["Ja_bebeu"]]

from sklearn.naive_bayes import CategoricalNB
from sklearn.model_selection import train_test_split 
from sklearn.metrics import accuracy_score
import numpy as np

SEED = 5
np.random.seed(SEED)
treino_x, teste_x, treino_y, teste_y = train_test_split(x, y, test_size = 0.25, stratify = y)

print("Treinaremos com %d elementos e testaremos com %d elementos" % (len(treino_x), len(teste_x)))

modelo = CategoricalNB()
modelo.fit(treino_x, treino_y)
previsoes = modelo.predict(teste_x)

acuracia = accuracy_score(teste_y, previsoes)*100
print("A acur√°cia foi %.2f%%" % acuracia)